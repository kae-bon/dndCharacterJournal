package com.kae.Models;

public class PcClass {
    private int id;
    private String name;

    public PcClass(String name, int id) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

}
